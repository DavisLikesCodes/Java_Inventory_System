    private void Login_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Login_btnMouseClicked
        // TODO add your handling code here:
       if(Uid_tb.getText().isEmpty()||Password_tb.getText().isEmpty())
       {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
       else{
            String Query = "select * from user where u_name='"+Uid_tb.getText()+"' and u_pass = '"+Password_tb.getText()+"'";
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost/inventory_db","root","");  
            St = Con.createStatement();
            Rs = St.executeQuery(Query);
            if (Rs.next()) 
            {
                new HomeForm().setVisible(true);
                this.dispose();
            }else
            {
                JOptionPane.showMessageDialog(this, "Wrong Username or Password");
            }
        } catch (Exception e) 
        {
            //e.printStackTrace();
        }
       }
    }//GEN-LAST:event_Login_btnMouseClicked

